'use client';

/**
 * Wave 60.68 — DropdownMenu primitive (Radix wrapper, shadcn-style).
 *
 * Material 3 menu pattern for contextual actions (row actions, profile menus,
 * overflow buttons). Backed by `@radix-ui/react-dropdown-menu` so we inherit
 * roving-focus keyboard a11y, ESC/outside-click dismiss, and portal rendering.
 *
 * Brand alignment: `bg-card` + `border-gold/20` + `text-foreground` matches
 * the existing Sheet/Popover/Dialog surfaces. Hover state uses gold/10 to
 * stay consistent with admin table-row hover.
 *
 * Re-exports follow shadcn-ui canonical naming so adoption is mechanical.
 */

import * as React from 'react';
import * as DropdownMenuPrimitive from '@radix-ui/react-dropdown-menu';
import { Check, ChevronRight, Circle } from 'lucide-react';
import { cn } from '../lib/utils';

export const DropdownMenu = DropdownMenuPrimitive.Root;
export const DropdownMenuTrigger = DropdownMenuPrimitive.Trigger;
export const DropdownMenuGroup = DropdownMenuPrimitive.Group;
export const DropdownMenuPortal = DropdownMenuPrimitive.Portal;
export const DropdownMenuSub = DropdownMenuPrimitive.Sub;
export const DropdownMenuRadioGroup = DropdownMenuPrimitive.RadioGroup;

export const DropdownMenuSubTrigger = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.SubTrigger>,
  React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.SubTrigger> & {
    inset?: boolean;
  }
>(function DropdownMenuSubTrigger({ className, inset, children, ...props }, ref) {
  return (
    <DropdownMenuPrimitive.SubTrigger
      ref={ref}
      className={cn(
        'flex cursor-default select-none items-center gap-2 rounded-sm px-2 py-1.5 text-sm outline-none',
        'focus:bg-gold/10 focus:text-foreground data-[state=open]:bg-gold/10',
        inset && 'pl-8',
        className,
      )}
      {...props}
    >
      {children}
      <ChevronRight className="ml-auto h-4 w-4" />
    </DropdownMenuPrimitive.SubTrigger>
  );
});

