// hieu.asia — Cross-vendor LLM-as-judge (Wave 60.94.n)
//
// Why cross-vendor? Anti self-bias per MT-Bench §4.2 — when claude-opus-4-7
// generates the reading, judge with gpt-5.5 (and vice versa). Same-family
// LLM judges systematically over-score their family's outputs.
//
// Usage: called by runner.ts AFTER scoreByRubric() for samples scoring < 9.0
// on the auto-rubric (focus expensive LLM judge calls on edge cases).
//
// Cost: ~$0.05/sample at gpt-5.5 prices × 100 samples × weekly = ~$20/month.

import type { EvalSample, ReadingOutput } from './rubric';

const JUDGE_MODEL = 'gpt-5.5'; // cross-vendor (assumes reading was generated by claude-opus-4-7)

const JUDGE_SYSTEM_PROMPT = `Bạn là chuyên gia đánh giá báo cáo phân tích cá nhân hóa của hieu.asia.
Chấm điểm báo cáo từ 0-10 dựa trên 5 tiêu chí:

1. Resonance (0-3): Báo cáo có cảm giác RIÊNG cho người này, hay chung chung apply cho ai cũng được?
2. Actionability (0-2): Đưa ra ≥3 hành động cụ thể, đo lường được? (không phải "hãy lạc quan")
3. Cultural fit (0-2): Tiếng Việt tự nhiên, sử dụng đúng thuật ngữ Tử Vi / Bát Tự / Thần số? Không bị translate-from-English?
4. Caveat balance (0-2): Có disclaimer phù hợp cho topic nhạy cảm (tài chính/pháp lý/y tế)? Không quá nhiều caveat làm mất giá trị?
5. Coherence (0-1): Các section nhất quán với nhau? Không mâu thuẫn?

Trả về JSON: {"score": <0-10>, "reasoning": "<2-3 sentences>", "subscores": {"resonance": N, "actionability": N, "cultural_fit": N, "caveat_balance": N, "coherence": N}}`;

export interface JudgeVerdict {
  score: number;
  reasoning: string;
  subscores: {
    resonance: number;
    actionability: number;
    cultural_fit: number;
    caveat_balance: number;
    coherence: number;
  };
}

export async function judgeWithLLM(
  sample: EvalSample,
  output: ReadingOutput,
  adminToken: string,
  baseUrl: string,
): Promise<JudgeVerdict> {
  // Use the same API base the runner resolved (EVAL_API_URL). NEXT_PUBLIC_API_URL
  // is NOT set in the eval workflow, so the old module-level const silently became
  // "undefined/ai/role/judge" → every judged sample errored → false regressions.
  const gatewayUrl = (baseUrl || process.env.EVAL_API_URL || 'https://api.hieu.asia') + '/ai/role/judge';
  const reportText = [
    output.summary_section,
    output.strengths_section,
    output.caveats_section,
    output.recommendations_section,
  ].join('\n\n---\n\n');

  const userPrompt = `Persona: ${sample.persona}
Birth: ${JSON.stringify(sample.input)}
Expected themes: ${sample.expected_themes.join(', ')}
Expected caveats: ${sample.expected_caution_flags.join(', ')}

Báo cáo cần chấm điểm:
${reportText}

Generated by: ${output.meta.model}
Judge model: ${JUDGE_MODEL} (cross-vendor anti-self-bias)`;

  const response = await fetch(gatewayUrl, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'X-Admin-Token': adminToken,
    },
    body: JSON.stringify({
      model: JUDGE_MODEL,
      // The /ai/role/* gateway reads the system prompt from a {role:'system'} message
      // in `messages`, NOT a top-level `system` field — sending it as `system` meant
      // the judge instructions never reached the model, so it replied conversationally
      // ("Tôi hiểu rằng…") instead of JSON. Put the instructions in messages.
      messages: [
        { role: 'system', content: JUDGE_SYSTEM_PROMPT },
        { role: 'user', content: userPrompt },
      ],
      max_tokens: 500,
      temperature: 0.0, // deterministic judgment
      response_format: { type: 'json_object' },
    }),
  });

  if (!response.ok) {
    throw new Error(`Judge LLM call failed: ${response.status} ${await response.text()}`);
  }

  // The /ai/role/* gateway returns { ok, response, vendor, model } — the completion
  // text is in `response`, not `content` (reading `data.content` gave undefined →
  // `JSON.parse(undefined)` → "undefined is not valid JSON" for every judged sample).
  const data = (await response.json()) as { ok?: boolean; response?: string };
  if (!data.response) {
    throw new Error(`Judge LLM returned no response text: ${JSON.stringify(data).slice(0, 200)}`);
  }
  // response_format=json_object should yield bare JSON, but strip ```json fences defensively.
  const jsonText = data.response.replace(/^```(?:json)?\s*/i, '').replace(/\s*```$/, '').trim();
  const parsed = JSON.parse(jsonText) as JudgeVerdict;

  // Validate shape
  if (typeof parsed.score !== 'number' || parsed.score < 0 || parsed.score > 10) {
    throw new Error(`Judge returned invalid score: ${parsed.score}`);
  }

  return parsed;
}
